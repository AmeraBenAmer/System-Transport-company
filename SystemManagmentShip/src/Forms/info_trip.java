    private void search_byMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_search_byMouseClicked
        // TODO add your handling code here:

        if (Search.getText().length() == 0) {
            Tools.msBox("please fill field ! ");
        } else {
            String strSearch = "select * from trip where ";
            if(tripno.isSelected()){
                strSearch+="trip_no";
            }else if(timebr.isSelected()){
                strSearch+="time_trip";
            }else if(br3.isSelected()){
                strSearch+="companyname";
            }
            else{
                strSearch+="shipname";
            }
            strSearch+=" like '%" + Search.getText() +"%'";
            database.UseDatabase.fillJTable(strSearch, jTable1);
        }
    }//GEN-LAST:event_search_byMouseClicked

