    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            // TODO add your handling code here:
            int row = jTable1.getSelectedRow();
            String C_Number = jTable1.getValueAt(row, 0).toString();
            trip.setText(C_Number);
            String time_t = jTable1.getValueAt(row, 8).toString();
            this.time.setText(time_t);
            String load = jTable1.getValueAt(row, 5).toString();
            loadwieght1.setText(load);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Date date_s = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(row, 6));
            Date_S.setDate(date_s);
            DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
            Date date1 = new SimpleDateFormat("yyyy-MM-dd").parse((String)model1.getValueAt(row, 7));
            Date_E.setDate(date1);
            String ship_s = jTable1.getValueAt(row, 2).toString();
            String f_seaport =jTable1.getValueAt(row, 3).toString();
            String t_seaport =jTable1.getValueAt(row, 4).toString();
            String companyn =jTable1.getValueAt(row, 1).toString();
            companyname.setSelectedItem(companyn);
            shipname.setSelectedItem(ship_s);
            fromseaport.setSelectedItem(f_seaport);
            toseaport.setSelectedItem(t_seaport);
        } catch (ParseException ex) {
            Logger.getLogger(info_trip.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_jTable1MouseClicked

